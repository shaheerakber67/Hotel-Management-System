    private void jTable_Display_RoomInfoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_Display_RoomInfoMouseClicked
        // TODO add your handling code here:
        int i=jTable_Display_RoomInfo.getSelectedRow();
        TableModel model=jTable_Display_RoomInfo.getModel();
        String floorno=model.getValueAt(i, 0).toString();
            switch(floorno){
                case "1":
                    jComboBox1.setSelectedIndex(0);
                    break;
                 case "2":
                    jComboBox1.setSelectedIndex(1);
                    break;
                    case "3":
                    jComboBox1.setSelectedIndex(2);
                    break;
                    case "4":
                    jComboBox1.setSelectedIndex(3);
                    break;
            }
            
        jTextField1.setText(model.getValueAt(i, 1).toString());
        String status=model.getValueAt(i, 2).toString();
        if(status.equals("Cleaned")){
            jRadioButton3.setSelected(true);
            }
             else{
                jRadioButton4.setSelected(true);
            }
        jTextField2.setText(model.getValueAt(i, 3).toString()); 
    }//GEN-LAST:event_jTable_Display_RoomInfoMouseClicked

