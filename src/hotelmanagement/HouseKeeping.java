    public ArrayList<RoomInfo> roominfolist() throws SQLException
{
    
    ArrayList<RoomInfo> roomsinfolist= new ArrayList<>();
     try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url="jdbc:sqlserver://localhost:1433;" +
           "databaseName=HOTEL_MANAGEMENT;user=shaheer;password=123";
            Connection con=DriverManager.getConnection(url);
            String query1="SELECT * from ROOMINFO";
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(query1);
              
            RoomInfo room_info;
            while(rs.next()){
                room_info=new RoomInfo(rs.getShort("Floor No"),rs.getShort("Room No"),rs.getString("STATUS"),rs.getString("ASSIGNED TO"));
                roomsinfolist.add(room_info);
  
            }      
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e); 
        }
    return roomsinfolist;
    
}
    public void show_RoomInfo() throws SQLException{
    
        ArrayList<RoomInfo> list=roominfolist();
        DefaultTableModel model= (DefaultTableModel)jTable_Display_RoomInfo.getModel();
        Object[] row=new Object[5];
        
        for(int i=0;i<list.size();i++){
        row[0]=list.get(i).getFloor_no();
        row[1]=list.get(i).getRoom_no();
        row[2]=list.get(i).getStatus();
        row[3]=list.get(i).getAssigned_to();
        model.addRow(row);
        }
}
