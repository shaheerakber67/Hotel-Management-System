CREATE DATABASE HOTEL_MANAGEMENT
USE HOTEL_MANAGEMENT
CREATE TABLE USERLOGIN(Users VARCHAR(15) CONSTRAINT USR_UN UNIQUE,Password VARCHAR(15) NOT NULL)
CREATE TABLE EMPLOYEES(ID VARCHAR(6) CONSTRAINT ID_PK PRIMARY KEY,NAME VARCHAR(20) NOT NULL, DESIGNATION VARCHAR(20),ADDRESS VARCHAR(50),TELEPHONE VARCHAR(15),"E-MAIL" VARCHAR(30),FAX VARCHAR(30))
CREATE TABLE ROOMS("ROOM NO" SMALLINT CONSTRAINT RM_PK PRIMARY KEY,"DATE Available" SMALLDATETIME,Category VARCHAR(30),Description VARCHAR(70),Price INT)
CREATE TABLE ROOMINFO("Floor No" SMALLINT NOT NULL,"Room No" SMALLINT FOREIGN KEY REFERENCES ROOMS("ROOM NO"),STATUS VARCHAR(20),"ASSIGNED TO" VARCHAR(30))
CREATE TABLE ROOM_RESERVATION("Conf No" INT IDENTITY(001,1) PRIMARY KEY,"Room No" SMALLINT FOREIGN KEY REFERENCES ROOMS("ROOM NO"),"Check-In Date" SMALLDATETIME, "Check-Out Date" SMALLDATETIME)
CREATE TABLE TOTALPAYMENT("Room No" SMALLINT FOREIGN KEY REFERENCES ROOMS("ROOM NO"), Price INT,"Days of Stay" INT)

